#import "UIColor+Addition.h"

// 页面背景色
UIColor *viewBgColor() {
    return [UIColor colorWithHexString:@"#0D0D0D"];
}

// cell的背景色
UIColor *cellBgColor() {
    return [UIColor colorWithHexString:@"#171717"];
}

// 线条颜色
UIColor *lineBgColor() {
    return [UIColor colorWithHexString:@"#333333"];
}

// 白色字体
UIColor *whiteTextColor() {
    return [UIColor colorWithHexString:@"#fefefe"];
}

// 灰色字体
UIColor *grayTextColor() {
    return [UIColor colorWithHexString:@"#8e8e92"];
}

// 红色字体
UIColor *redTextColor() {
    return [UIColor colorWithHexString:@"#ea4e3d"];
}
